class I18nManager {
    constructor() {
        this.currentLang = localStorage.getItem('webocontrol_lang') || 'en';
        this.init();
    }

    init() {
        this.updateUI();
        this.applyDirection();
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('webocontrol_lang', lang);
        this.updateUI();
        this.applyDirection();
        // Dispatch event for components that need to re-render
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
    }

    applyDirection() {
        document.documentElement.lang = this.currentLang;
        document.body.dir = this.currentLang === 'ar' ? 'rtl' : 'ltr';
        if (this.currentLang === 'ar') {
            document.body.classList.add('lang-ar');
        } else {
            document.body.classList.remove('lang-ar');
        }
    }

    updateUI() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = translations[this.currentLang][key];
            if (translation) {
                // Check if translation contains HTML tags
                if (translation.includes('<') && translation.includes('>')) {
                    el.innerHTML = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });

        const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        placeholders.forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (translations[this.currentLang][key]) {
                el.placeholder = translations[this.currentLang][key];
            }
        });

        // Update active class on lang switcher buttons
        const buttons = document.querySelectorAll('.lang-btn');
        buttons.forEach(btn => {
            const onclick = btn.getAttribute('onclick');
            if (onclick) {
                const match = onclick.match(/'(.*?)'/);
                if (match) {
                    const lang = match[1];
                    if (lang === this.currentLang) {
                        btn.classList.add('active');
                    } else {
                        btn.classList.remove('active');
                    }
                }
            }
        });
    }

    t(key) {
        return translations[this.currentLang][key] || key;
    }
}

window.i18n = new I18nManager();
